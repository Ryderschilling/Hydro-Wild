async function updateSitemap() {
  const xml = await readFile(SITEMAP_PATH, 'utf-8');
  const startIdx = xml.indexOf(SITEMAP_START);
  const endIdx = xml.indexOf(SITEMAP_END);
  if (startIdx === -1 || endIdx === -1) {
    throw new Error(
      `generate-blog-posts: sitemap.xml is missing the "${SITEMAP_START.trim()}" / "${SITEMAP_END.trim()}" markers`
    );
  }

  const entries = POSTS.map((post) => {
    const lastmod = post.dateModified ? toISODate(post.dateModified) : toISODate(post.date);
    return `  <url><loc>${SITE}/blog/${post.slug}/</loc><lastmod>${lastmod}</lastmod><changefreq>monthly</changefreq><priority>0.5</priority></url>`;
  }).join('\n');

  const updated =
    xml.slice(0, startIdx + SITEMAP_START.length) +
    (entries ? `\n${entries}\n  ` : '\n  ') +
    xml.slice(endIdx);

  await writeFile(SITEMAP_PATH, updated, 'utf-8');
  console.log(`✓ public/sitemap.xml (${POSTS.length} blog post${POSTS.length === 1 ? '' : 's'})`);
}

async function main() {
  for (const post of POSTS) {
    const dir = resolve(ROOT, 'blog', post.slug);
    await mkdir(dir, { recursive: true });
    await writeFile(resolve(dir, 'index.html'), renderPost(post), 'utf-8');
    console.log(`✓ blog/${post.slug}/index.html`);
  }
  await updateSitemap();
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
